    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       
         
         DefaultListModel<String> model = (DefaultListModel<String>) jList1
                .getModel();
         
         int click=JOptionPane.showConfirmDialog(rootPane,"Bạn chắc chắn muốn xóa chứ? ");
         if(click==JOptionPane.OK_OPTION){
         String i=jList1.getSelectedValue().toString();
         if (!model.isEmpty() && jList1.getSelectedIndex() >= 0) {
            model.remove(jList1.getSelectedIndex());
            Dictionary.store.remove(i);
            JOptionPane.showMessageDialog(rootPane, "Bạn đã xóa thành công");
            jTextArea1.setText("");
        }
         }
         jTextField1.setText("");
       
        jList1.setModel(model);
        
        DictionaryManagement dm=new DictionaryManagement();
        dm.exportToFile();
         
       
        
        
       
     // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

